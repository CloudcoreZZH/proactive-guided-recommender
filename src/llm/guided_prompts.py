"""主动引导式推荐使用的 prompts"""

# 把一个抽象关键词（如"教育"、"治愈系"）扩展为一段语义丰富的描述，
# 让 BGE 向量编码拥有更充分的主题上下文，避免单词向量太稀疏。
TOPIC_EXPANSION_PROMPT = """请对下面的主题关键词，生成 120 字左右的语义描述，用于电影语义检索：
主题：{keyword}

描述应当包含：
1. 这个主题常见的内容范畴与常见子类型；
2. 典型的情感基调、叙事风格或表现手法；
3. 此类电影常见的观众价值（如启迪、知识普及、情感共鸣等）。

直接返回描述正文，不要加标题/序号/列表。"""


# 让 LLM 在"用户兴趣 × 目标主题"的双约束下挑选电影，并给出解释。
# 这是主动引导的关键：推荐既要贴合用户口味，又要把他引导到新主题。
GUIDED_RANKING_PROMPT = """你是一个主动引导型电影推荐助手。

用户的兴趣画像：
{user_profile}

要把用户引导去探索的目标主题：「{target_topic}」
主题语义描述：{topic_description}
{avoid_block}
以下是语义检索给出的候选电影（已排除用户看过的）：
{candidate_movies}

请从候选中精心挑选 {top_k} 部最合适的电影，这些电影应当同时满足：
1. **承接用户原有兴趣** —— 保留用户已有品味中的某个元素（类型、氛围、时代、情绪节奏等），让用户愿意接受；
2. **指向目标主题** —— 对「{target_topic}」主题有明显的内容关联或探索价值；
3. **避免清单所列的元素**（如果有的话），可替换为同等情感强度的替代方案。

严格按下述格式返回，**每行一部电影**，一共 {top_k} 行：
1. [电影ID] 电影名 —— 推荐理由（40字以内，必须同时说出"承接了用户的X"和"引导到Y主题"两层含义）
2. [电影ID] 电影名 —— ……
...
"""


# 用于从自由文本兴趣生成简短画像（比用户自己写的更结构化）
FREE_TEXT_PROFILE_PROMPT = """以下是用户对自己观影偏好的自由描述：
"{raw_text}"

请把它整理成 80 字左右、适合作为语义检索查询的结构化画像，覆盖：
偏好类型、偏好氛围/节奏、典型代表作（如果提到过）。
直接输出正文。"""
